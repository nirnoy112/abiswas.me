<h3>Introduction</h3>
<p>Nicole is a social entrepreneur who is passionate about levelling life's playing-field by making education and medicine accessible to everyone regardless of gender, race, geography, or background.</p>
<p>Nicole specialises in research-driven product development. She dives deep into a problem, understanding its complexities and subtleties, and designs innovative people-focussed solutions. She has led the product development of a number of projects including Meducation, Exercism, Kaido, and Reachora. Over 200,000 people have benefited from free educational products that Nicole has developed.</p>
<p>Nicole is the co-founder of Thalamus where she leads a variety of projects, ranging from mentor-based learning for junior software developers to artificial intelligence used during diagnostic heart surgery. Thalamus is a profitable, independently owned company.</p>
